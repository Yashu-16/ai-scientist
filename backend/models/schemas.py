# ── Multi-Disease Comparison ──────────────────────────────────
class DiseaseComparisonEntry(BaseModel):
    """Score for one drug across one disease."""
    disease_name:    str
    final_score:     float = 0.0
    confidence:      float = 0.0
    risk_level:      str   = "Unknown"
    hypothesis_title:str   = ""


class DrugComparisonRow(BaseModel):
    """One drug compared across multiple diseases."""
    drug_name:       str
    target_protein:  str                      = ""
    mechanism:       str                      = ""
    diseases:        List[DiseaseComparisonEntry] = []
    overlap_score:   float = 0.0   # How consistent across diseases (0-1)
    avg_score:       float = 0.0   # Average final_score across diseases
    appears_in:      int   = 0     # How many diseases this drug appears in


class SharedProtein(BaseModel):
    """A protein appearing in multiple disease analyses."""
    gene_symbol:        str
    protein_name:       str
    diseases:           List[str]  = []
    avg_association:    float      = 0.0
    appears_in:         int        = 0


class MultiDiseaseComparison(BaseModel):
    """Full multi-disease comparison result."""
    diseases_analyzed:  List[str]              = []
    shared_proteins:    List[SharedProtein]    = []
    drug_comparison:    List[DrugComparisonRow]= []
    total_shared_proteins: int                 = 0
    total_shared_drugs:    int                 = 0
    repurposing_opportunities: List[str]       = []


# ── Multi-Disease Request ─────────────────────────────────────
class MultiDiseaseRequest(BaseModel):
    diseases:    List[str] = Field(
        ...,
        min_length=2,
        example=["Alzheimer disease", "Parkinson disease"]
    )
    max_targets: int = Field(default=5, ge=1, le=10)
    max_papers:  int = Field(default=4, ge=1, le=10)
    max_drugs:   int = Field(default=3, ge=1, le=5)


# ── Hypothesis ────────────────────────────────────────────────
